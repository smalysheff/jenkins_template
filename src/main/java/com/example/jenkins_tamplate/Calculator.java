package com.example.jenkins_tamplate;

public class Calculator {

    public long add(int a, int b) {
        return a + b;
    }

    public long sub(int a, int b) {
        return a - b;
    }

    public long multi(int a, int b) {
        return (long) a * b;
    }

    public long div(int a, int b) {
        return a / b;
    }
}
